using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Identity;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.Extensions.Logging;

namespace Project_Ceustermans_Robin.Areas.Identity.Pages.Account.Manage
{
    public class Disable2faModel : PageModel
    {
        private readonly UserManager<IdentityUser> _userManager;
        private readonly ILogger<Disable2faModel> _logger;

        public Disable2faModel(
            UserManager<IdentityUser> userManager,
            ILogger<Disable2faModel> logger)
        {
            _userManager = userManager;
            _logger = logger;
        }

        [TempData]
        public string StatusMessage { get; set; }

        public async Task<IActionResult> OnGet()
        {
            var user = await _userManager.GetUserAsync(User);
            if (user == null)
            {
                return NotFound($"Niet mogelijk om gebruiker in te laten met ID '{_userManager.GetUserId(User)}'.");
            }

            if (!await _userManager.GetTwoFactorEnabledAsync(user))
            {
                throw new InvalidOperationException($"Kan two-factor authenticatie niet uitschakelen voor gebruiker met  ID '{_userManager.GetUserId(User)}' as it's not currently enabled.");
            }

            return Page();
        }

        public async Task<IActionResult> OnPostAsync()
        {
            var user = await _userManager.GetUserAsync(User);
            if (user == null)
            {
                return NotFound($"Niet mogelijk om gebruiker te laden met ID '{_userManager.GetUserId(User)}'.");
            }

            var disable2faResult = await _userManager.SetTwoFactorEnabledAsync(user, false);
            if (!disable2faResult.Succeeded)
            {
                throw new InvalidOperationException($"Onverwachtie error opgetreden bij het uitschakelen van two-factor authenticatie van gebruiker met ID '{_userManager.GetUserId(User)}'.");
            }

            _logger.LogInformation("Gebruiker met ID '{UserId}' heeft two-factor authenticatie uitgeschakeld.", _userManager.GetUserId(User));
            StatusMessage = "two-factor authenticatie is uitgeschakeld. u Kan two-factor authenticatie terug inschakelen wanneer u een authenticator app instelt.";
            return RedirectToPage("./TwoFactorAuthentication");
        }
    }
}